package net.todd.biblestudy.db;

public class Verse
{
	private Integer verseId;
	private String text;
	
	public Integer getVerseId()
	{
		return verseId;
	}
	public void setVerseId(Integer verseId)
	{
		this.verseId = verseId;
	}
	public String getText()
	{
		return text;
	}
	public void setText(String text)
	{
		this.text = text;
	}
}
