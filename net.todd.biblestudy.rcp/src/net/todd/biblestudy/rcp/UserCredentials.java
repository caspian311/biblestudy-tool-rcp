package net.todd.biblestudy.rcp;

public class UserCredentials
{
	private String user;
	private String pass;

	public void setUser(String user)
	{
		this.user = user;
	}

	public void setPass(String pass)
	{
		this.pass = pass;
	}

	public String getUser()
	{
		return user;
	}

	public String getPass()
	{
		return pass;
	}
}